    private void srcKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_srcKeyReleased
        // TODO add your handling code here:
        final DefaultTableModel model = (DefaultTableModel) listTable.getModel();
        final TableModel tm = listTable.getModel();
        int t = listTable.getRowCount();

//listTable.scrollRectToVisible(null);
        //listTable.clearSelection();
        //System.out.println("RowCount:"+t);
        String tb;
        tb = src.getText();

        //top.setText("1"+tb);
        if (!tb.isEmpty()) {
            int k = evt.getKeyCode();
            if (k == 10) {
                i = listTable.getSelectedRow();
                //System.out.println("Eneter is Pressed::"+i);
                for (++i; i < t; ++i) {
                        //System.out.println("i="+i);
                    //tb=src.getText();
                    //top.setText("3"+tb);
                    String n = (String) tm.getValueAt(i, 2);
                    n = n.toUpperCase();
                    tb = tb.toUpperCase();
                    //System.out.println("tb="+tb+"   n="+n);
                    if (n.contains(tb)) {
                        //System.out.println("yes:"+tb+":::"+i);
                        listTable.setRowSelectionInterval(i, i);
                        listTable.getSelectionModel().setSelectionInterval(i, i);
                        listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                        break;
                        //listTable.selectAll();
                    } else {
                        String m = (String) tm.getValueAt(i, 2);
                        m = m.toUpperCase();
                        if (m.contains(tb)) {       //code
                            listTable.setRowSelectionInterval(i, i);
                            listTable.getSelectionModel().setSelectionInterval(i, i);
                            listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                            break;
                        } else {
                            String c = (String) tm.getValueAt(i, 1);
                            c = c.toUpperCase();
                            if (c.contains(tb)) {       //mobile no
                                listTable.setRowSelectionInterval(i, i);
                                listTable.getSelectionModel().setSelectionInterval(i, i);
                                listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                                break;
                            } else {

                                String hn = (String) tm.getValueAt(i, 3);
                                hn = hn.toUpperCase();
                                if (hn.contains(tb)) {       //mobile no
                                    listTable.setRowSelectionInterval(i, i);
                                    listTable.getSelectionModel().setSelectionInterval(i, i);
                                    listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                                    break;
                                } else {

                                    listTable.clearSelection();
                                //System.out.println("nulli:"+listTable.getSelectedRow());
                                    //src.requestFocus();
                                    //listTable.setRowSelectionInterval(3, 3);
                                }
                            }
                        }
                    }

                }
                if (i == t) {
                    listTable.setRowSelectionInterval(0, 0);
                    listTable.getSelectionModel().setSelectionInterval(0, 0);
                    listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(0, 0, true)));
                    listTable.clearSelection();
                }
            } else {
                listTable.setRowSelectionInterval(0, 0);
                listTable.getSelectionModel().setSelectionInterval(0, 0);
                listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(0, 0, true)));
                listTable.clearSelection();
                        //tb=src.getText();
                //top.setText("2"+tb);
                i = listTable.getSelectedRow();
                for (++i; i < t; ++i) {
                    //tb=src.getText();
                    //top.setText("3"+i);
                    String n = (String) tm.getValueAt(i, 2);
                    n = n.toUpperCase();
                    tb = tb.toUpperCase();
                    //System.out.println("tb="+tb+"   n="+n);
                    if (n.contains(tb)) {
                        //System.out.println("yes:"+tb+":::"+i);
                        listTable.setRowSelectionInterval(i, i);
                        listTable.getSelectionModel().setSelectionInterval(i, i);
                        listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                        break;
                        //listTable.selectAll();
                    } else {
                        String m = (String) tm.getValueAt(i, 3);
                        m = m.toUpperCase();
                        if (m.contains(tb)) {       //code
                            listTable.setRowSelectionInterval(i, i);
                            listTable.getSelectionModel().setSelectionInterval(i, i);
                            listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                            break;
                        } else {
                            String c = (String) tm.getValueAt(i, 1);
                            c = c.toUpperCase();
                            if (c.contains(tb)) {       //mobile no
                                listTable.setRowSelectionInterval(i, i);
                                listTable.getSelectionModel().setSelectionInterval(i, i);
                                listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(i + 5, 0, true)));
                                break;
                            } else {
                                listTable.clearSelection();
                                //System.out.println("nulli:"+listTable.getSelectedRow());
                                //src.requestFocus();
                                //listTable.setRowSelectionInterval(3, 3);
                            }
                        }
                    }

                }
                if (i == t) {
                    listTable.setRowSelectionInterval(0, 0);
                    listTable.getSelectionModel().setSelectionInterval(0, 0);
                    listTable.scrollRectToVisible(new Rectangle(listTable.getCellRect(0, 0, true)));
                    listTable.clearSelection();
                }
            }
        }
    }//GEN-LAST:event_srcKeyReleased

