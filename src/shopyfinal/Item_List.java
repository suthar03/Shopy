    public Item_List(String base, String golo, String idy, String sy, String ey) {
        baseadd = base;
        logo = golo;
        IDyear = idy;
        Syear = sy;
        Eyear = ey;
        try {
            user = ShortFunction.GetLine(baseadd + "/Cuser.txt", 2);
        } catch (IOException ex) {
            JLabel label = new JLabel("Current user !!! Error0003");
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.WARNING_MESSAGE);

        }
        ITMadd = baseadd + "/" + user + "/" + IDyear + "/item";
        initComponents();
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        setBounds(50, 50, 770, (int) dim.getHeight() - 50);
        this.setLocationRelativeTo(null);
        try {
            this.setIconImage(ImageIO.read(new File(logo)));
        } catch (IOException ex) {
            JLabel label = new JLabel("Logo Problem !!! Error0001");
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.WARNING_MESSAGE);

        }

        try {
            Robot r = new Robot();
            r.keyPress(KeyEvent.VK_ALT);
            r.keyPress(KeyEvent.VK_SHIFT);
            r.keyPress(KeyEvent.VK_0);
            r.keyRelease(KeyEvent.VK_0);
            r.keyRelease(KeyEvent.VK_SHIFT);
            r.keyRelease(KeyEvent.VK_ALT);
        } catch (AWTException ex) {
            JLabel label = new JLabel("Language Setup Error !!");
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null, label, "ERROR", JOptionPane.WARNING_MESSAGE);
        }
//#########################################################################################################################################3
        DefaultTableModel model = (DefaultTableModel) listTable.getModel();
        Scanner read;
        try {
            read = new Scanner(new File(ITMadd + "/item.txt"), "UTF-8");
            read.useDelimiter("\\n");

            String code, Ename, Hname, rate;
            while (read.hasNext()) {

                //TextIO.put(read.hasNext());
                String Pacc = new String(read.nextLine());
                Scanner inread;
                try {
                    inread = new Scanner(new File(ITMadd + "/" + Pacc + "/profile.txt"), "UTF-8");
                    inread.useDelimiter("\\n");
                    code = inread.nextLine();
                    Ename = inread.nextLine();
                    Hname = inread.nextLine();
                    rate = inread.nextLine();

                    model.addRow(new Object[]{listTable.getRowCount() + 1, code, Ename, Hname, rate});
                    //TextIO.putln(i);
                    inread.close();
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Item_List.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            read.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Item_List.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

