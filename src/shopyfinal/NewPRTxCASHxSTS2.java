    public String stringl(String A,int sz){
    String B = null;
    int si = A.length();
    if(sz<si)
        B = A.substring(0, sz);
    else{
        B=A;
        for(int sp=0;sp<sz-si;++sp){
            B=B+" ";
        }
    }
        return B;
}
