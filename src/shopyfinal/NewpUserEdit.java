    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed

        String pwdstr = new String(pwd.getPassword());
                        String cpwdstr = new String(cpwd.getPassword());
                        if(pwdstr.equals(cpwdstr)){
                            String data = usr.getText();
                            data  =data+ new String(pwd.getPassword());
                            String fname =baseadd+"/"+ usr.getText()+"/"+usr.getText()+".txt";
                            FileWriter fw;
                                        try {
                                            fw = new FileWriter(fname);
                                            fw.write(data);
                                            fw.close();
                                            this.dispose();
                                            MessageOnTimeOfSuccess.mainn("PassWord Changes Successfully");
                                        } catch (IOException ex) {
                                            JLabel label = new JLabel("Something Wrong"+ex);
                        label.setFont(new Font("Arial", Font.BOLD, 18));
                        JOptionPane.showMessageDialog(null,label,"ERROR",JOptionPane.ERROR_MESSAGE);
                                            //errmessage.mainn("Something Wrong", 200, 200);
                                        }


                        }
                        else{
                            pwd.requestFocus();
                            JLabel label = new JLabel("Password are not matching with each other !!!");
                        label.setFont(new Font("Arial", Font.BOLD, 18));
                        JOptionPane.showMessageDialog(null,label,"ERROR",JOptionPane.ERROR_MESSAGE);
                            //errmessage.mainn(, 400, 200);
                        }
                        
    }//GEN-LAST:event_confirmActionPerformed

